class DeviceInfoCubit extends Cubit<DeviceInfoState> {
  DeviceInfoCubit() : super(DeviceInfoInitial({}));

  Future<Map<String, dynamic>> getPlatformState() async {
    emit(const DeviceInfoLoading({}));
    var deviceData = <String, dynamic>{};
    final DeviceInfoPlugin deviceInfoPlugin = DeviceInfoPlugin();
    Map<String, dynamic> _deviceData = <String, dynamic>{};

    try {
      if (kIsWeb) {
        deviceData = DeviceModel.ReadWebBrowserInfo(
            await deviceInfoPlugin.webBrowserInfo);
      } else {
        final info = NetworkInfo();

        _deviceData["wifi_Name"] = await info.getWifiName(); // "FooNetwork"
        _deviceData["wifi_BSSID"] =
            await info.getWifiBSSID(); // 11:22:33:44:55:66
        _deviceData["wifi_BSSID"] = _deviceData["wifi_BSSID"]
            .substring(0, _deviceData["wifi_BSSID"].toString().length - 1);
        _deviceData["wifi_IP"] = await info.getWifiIP(); // 192.168.1.43
        if (Platform.isAndroid) {
          deviceData = DeviceModel.ReadAndroidBuildData(
              await deviceInfoPlugin.androidInfo);
        } else if (Platform.isIOS) {
          deviceData =
              DeviceModel.ReadIosDeviceInfo(await deviceInfoPlugin.iosInfo);
        } else if (Platform.isLinux) {
          deviceData =
              DeviceModel.ReadLinuxDeviceInfo(await deviceInfoPlugin.linuxInfo);
        } else if (Platform.isMacOS) {
          deviceData =
              DeviceModel.ReadMacOsDeviceInfo(await deviceInfoPlugin.macOsInfo);
        } else if (Platform.isWindows) {
          deviceData = DeviceModel.ReadWindowsDeviceInfo(
              await deviceInfoPlugin.windowsInfo);
        }
      }
    } on PlatformException {
      deviceData = <String, dynamic>{
        'Error:': 'Failed to get platform version.'
      };
    }
    emit(DeviceInfoDone(_deviceData..addAll(deviceData)));
    return deviceData;
  }
}

